    pub fn fromSubopcode(value: u32) ?Op {
        if (value > 0x113) return null;
        return std.enums.fromInt(Op, @as(u16, @intCast(value)));
    }

    pub fn isRelaxed(self: Op) bool {
        return @intFromEnum(self) >= 0x100;
    }

    pub fn immediate(self: Op) Immediate {
        return switch (self) {
            .v128_load, .v128_load8x8_s, .v128_load8x8_u, .v128_load16x4_s, .v128_load16x4_u, .v128_load32x2_s, .v128_load32x2_u, .v128_load8_splat, .v128_load16_splat, .v128_load32_splat, .v128_load64_splat, .v128_store, .v128_load32_zero, .v128_load64_zero => .memarg,
            .v128_const => .v128,
            .i8x16_shuffle => .lane16,
            .i8x16_extract_lane_s, .i8x16_extract_lane_u, .i8x16_replace_lane, .i16x8_extract_lane_s, .i16x8_extract_lane_u, .i16x8_replace_lane, .i32x4_extract_lane, .i32x4_replace_lane, .i64x2_extract_lane, .i64x2_replace_lane, .f32x4_extract_lane, .f32x4_replace_lane, .f64x2_extract_lane, .f64x2_replace_lane => .lane,
            .v128_load8_lane, .v128_load16_lane, .v128_load32_lane, .v128_load64_lane, .v128_store8_lane, .v128_store16_lane, .v128_store32_lane, .v128_store64_lane => .memarg_lane,
            else => .none,
        };
    }
